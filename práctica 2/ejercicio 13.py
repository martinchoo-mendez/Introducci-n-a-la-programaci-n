#Escribir un programa que pida al usuario dos enteros y que luego muestre si el primero es
#mayor que el segundo o viceversa.

entero1=int(input("por favor, ingrese un entero: "))
entero2=int(input("por favor, ingrese otro entero: "))

if entero1>entero2:
    print("el mayor es: ", entero1)
else:
    print("el mayor es: ", entero2)